			$class = __CLASS__ ;
			self::$obj[$conf] = new $class( $conf );
		}
		return self::$obj[$conf];
	}

	public $alipay_config =  array();

	public function __construct( $conf = 'default' ){

		$this->alipay_config = $this->getConf( $conf );
	}

	public function getConf( $conf ){
		return CbaseInit::app()->config['alipay'][$conf];
	}


	// 提交订单
	public function AlipaySubmit( $params ,  $method = 'POST' ){

		//构造要请求的参数数组，无需改动
		$parameter = array(
			"service" => "create_direct_pay_by_user",
			"partner" => trim($this->alipay_config['partner']),
			"seller_email" => trim($this->alipay_config['seller_email']),
			"payment_type"	=> 1 ,
			"out_trade_no"	=> $params['code'],  // 商户订单号
			"subject"		=> $params['title'],  // 订单名称
			"total_fee"		=> $params['price'],  // 订单金额
			"body"			=> $params['desc'],   // 订单描述
			"show_url"		=> $params['show_url'],  //商品展示地址
			"anti_phishing_key"	=> '',
			"exter_invoke_ip"	=> '',
			"_input_charset"	=> trim(strtolower($this->alipay_config['input_charset']))
		);		
		$parameter['notify_url'] = isset($params['notify_url'])?$params['notify_url']:$this->alipay_config['notify_url'];
		$parameter['return_url'] = isset($params['return_url'])?$params['return_url']:$this->alipay_config['return_url'];
		echo $this->buildRequestForm($parameter,$method, "确认");
	}


